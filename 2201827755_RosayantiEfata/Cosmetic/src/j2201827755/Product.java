package j2201827755;

public class Product {
	private String name;
	private String category;
	private int priceForCust;
	private int priceForSales;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public int getPriceForCust() {
		return priceForCust;
	}

	public void setPriceForCust(int priceForCust) {
		this.priceForCust = priceForCust;
	}

	public int getPriceForSales() {
		return priceForSales;
	}

	public void setPriceForSales(int priceForSales) {
		this.priceForSales = priceForSales;
	}

}
