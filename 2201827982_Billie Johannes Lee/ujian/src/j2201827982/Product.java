package j2201827982;

public class Product {

	private long price;
	private int stock;

	public Product() {
		this.stock = 100;
	}

	public long getPrice() {
		return price;
	}

	public void setPrice(long price) {
		this.price = price;
	}

	public int getStock() {
		return stock;
	}

	public void setStock(int stock) {
		this.stock = stock;
	}

}
